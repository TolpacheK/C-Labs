#include <iostream>
#include <cmath>
#include <string>

int main() {

    double x, y;

    // Запит координат
    std::cout << "Введіть координату x: ";
    if (!(std::cin >> x)) {
        // Обробка помилки введення
        std::cerr << "Помилка введення x.\n";
        return 1;
    }

    std::cout << "Введіть координату y: ";
    if (!(std::cin >> y)) {
        // Обробка помилки введення
        std::cerr << "Помилка введення y.\n";
        return 1;
    }
    
    // Умова для трикутника 
    bool in_triangle = (x >= 0.0 && y >= 0.0 && (x + y <= 1.0));

    // Умова для чверті круга
    bool in_semicircle = (x <= 0.0 && (x*x + y*y <= 1.0));

    // Точка потрапляє в область, якщо виконується умова для трикутника АБО для чверті круга.
    if (in_triangle || in_semicircle) {
        std::cout << "Результат: Точка (" << x << ", " << y << ") ПОТРАПЛЯЄ в заштриховану область.\n";
    } else {
        std::cout << "Результат: Точка (" << x << ", " << y << ") НЕ ПОТРАПЛЯЄ в заштриховану область.\n";
    }

    return 0;
}
